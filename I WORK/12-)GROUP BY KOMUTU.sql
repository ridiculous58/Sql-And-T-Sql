--SELECT * FROM CUSTOMERS
SELECT CITY,
SUM(TOTALPRICE) AS TOTALPRICE,
COUNT(ID) AS ROWCOUNT_,
MIN(TOTALPRICE) AS  MINPRICE,
MAX(TOTALPRICE) AS  MAXPRICE,
AVG(TOTALPRICE) AS  AVEGRATE
FROM SALES
GROUP BY CITY
ORDER BY SUM(TOTALPRICE) DESC

