--CITY TABLOSUNA GORE TOTOLPRICE TOPLAMININ EN YUKSEK OLAN ILK 10 DEGERI SIRALA
SELECT TOP 10 PERCENT
CITY,
SUM(TOTALPRICE) AS TOTALPRICE
FROM SALES
GROUP BY CITY
ORDER BY SUM(TOTALPRICE) DESC

